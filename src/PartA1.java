public class PartA1 {
    public static void main(String[] args) {

        //Task 1
        for(int r=0;r<=30;r++)
        {
            System.out.print(r + " ");
        }
        System.out.println("\n");

        //Task 2
        for(int r=30;r>=0;r--)
            {
            System.out.print(r + " ");
            }
        System.out.println("\n");

        //Task 3
        for(int r=-0;r<=18;r+=3)
            {
            System.out.print(r + " ");
            }
        System.out.println("\n");
    }
}
